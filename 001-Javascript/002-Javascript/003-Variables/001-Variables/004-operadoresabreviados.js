//Resto 2 a la variable
edad -= 2;
console.log("Tu edad es de: "+edad+" años");
//Multiplico 2 a la variable
edad *= 2;
console.log("Tu edad es de: "+edad+" años");
//Divido 2 a la variable
edad /= 2;
